import UIKit

extension UIFont {
    enum CustomFonts {
        static let large = UIFont.boldSystemFont(ofSize: 26)
        static let mediumBold = UIFont.systemFont(ofSize: 22, weight: .medium)
        static let medium = UIFont.systemFont(ofSize: 22, weight: .regular)
    }
}
